class arithmetic:
    
    def __init__(self):
        pass
        
    def add(self, x: int, y: int) -> int:
        return x + y
    
    def sub(self, x: int, y: int) -> int:
        return x - y
    
    def mul(self, x: int, y: int) -> int:
        return x * y
    
    def div(self, x: int, y: int) -> int:
        return x / y